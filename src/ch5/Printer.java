package ch5;

public class Printer {
	
	int printerln(int i) {
		System.out.println(i);
		return i;
	}
	
	boolean printerln(boolean b) {
		System.out.println(b);
		return b;
	}
	
	double printerln(double d) {
		System.out.println(d);
		return d;
	}
	
	String printerln(String s) {
		System.out.println(s);
		return s;
	}
	
	char printerln(char c) {
		System.out.println(c);
		return c;
	}
	
}
